package com.example.myecomerceapp.Model;

public class Users  {
    private String firstname,lastname,addrees,password,email,mobile;

    public Users(String firstname, String lastname, String addrees, String password, String email, String mobile) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.addrees = addrees;
        this.password = password;
        this.email = email;
        this.mobile = mobile;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getAddrees() {
        return addrees;
    }

    public void setAddrees(String addrees) {
        this.addrees = addrees;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }
}
